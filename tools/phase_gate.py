def _check_bof_no_string_padding(paths: List[Path]) -> CheckResult:
    """Verify BOF modules don't use 'A'*N as payload string."""
    bof_paths = [p for p in paths if "bof" in p.stem or "shellz" in p.stem]
    bad = []
    for path in bof_paths:
        if not path.exists():
            continue
        src = _read_file(path)
        if re.search(r"[\"'][A-Za-z][\"']\s*\*\s*\d+", src):
            bad.append(path.name)
    if bad:
        return _fail("bof_no_string_padding", f"String padding payload (use struct/bytes instead): {bad}")
