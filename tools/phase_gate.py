def _check_bof_payload_bytes(paths: List[Path]) -> CheckResult:
    """Verify BOF modules use struct.pack or bytes literals for payload construction."""
    high_cvss_bof = [p for p in paths if any(kw in p.stem for kw in (
        "bof", "overflow", "shellz", "wolfssl", "panos_userid"
    ))]
    bad = []
    for path in high_cvss_bof:
        if not path.exists():
            continue
        src = _read_file(path)
        has_bytes = any(token in src for token in ("struct.pack", "b'\\x", 'b"\\x', "bytearray(", "bytes(["))
        if not has_bytes:
            bad.append(path.name)
    if bad:
        return _fail("bof_payload_bytes", f"Missing struct/bytes payload construction in: {bad}")
    return _ok("bof_payload_bytes")


def _check_default_creds_pairs(paths: List[Path]) -> CheckResult:
    """Verify default-creds modules iterate at least 3 pairs."""
    cred_paths = [p for p in paths if any(kw in p.stem for kw in ("creds", "default", "enet"))]
    bad = []
    for path in cred_paths:
        if not path.exists():
            continue
        src = _read_file(path)
        pairs = re.findall(r"['\"][^'\"]+:[^'\"]+['\"]", src)
        cred_pairs = [p for p in pairs if ":" in p and not p.startswith("'http") and not p.startswith('"http')]
        if len(cred_pairs) < 3:
            bad.append(f"{path.name} ({len(cred_pairs)} pairs found)")
    if bad:
        return _fail("creds_pairs", f"Fewer than 3 credential pairs in: {bad}")
    return _ok("creds_pairs")


