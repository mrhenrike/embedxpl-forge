def _print_result(r: CheckResult) -> None:
    mark = "\033[92m PASS \033[0m" if r.passed else "\033[91m FAIL \033[0m"
    label = r.name[:40].ljust(42)
    msg = f"  {r.message[:90]}" if r.message else ""
    print(f"  [{mark}] {label}{msg}")


def run_gate(name: str) -> bool:
    fn = _GATE_REGISTRY.get(name)
    if fn is None:
        print(f"Unknown gate: {name}")
        return False
    print(f"\n\033[1m\033[96m{'='*60}\033[0m")
    print(f"\033[1m\033[96m  GATE {name}\033[0m")
    print(f"\033[1m\033[96m{'='*60}\033[0m")
    results = fn()
    passed = sum(1 for r in results if r.passed)
    total = len(results)
    for r in results:
        _print_result(r)
    print(f"\n\033[1mResults: {passed}/{total} passed\033[0m", end="  ")
    if passed == total:
        print(f"\033[92m(all passed)\033[0m")
        print(f"\n\033[92m\033[1mGate {name} PASSED.\033[0m\n")
        return True
    else:
        failed = [r.name for r in results if not r.passed]
        print(f"\033[91m(FAILED: {failed[:5]})\033[0m")
        print(f"\n\033[91m\033[1mGate {name} FAILED. Fix all issues before proceeding.\033[0m\n")
        return False


def main() -> None:
    parser = argparse.ArgumentParser(description="EmbedXPL-Forge Quality Gates")
    parser.add_argument("--phase", metavar="N",
                        help=f"Run specific gate: {', '.join(_GATE_REGISTRY)}")
    parser.add_argument("--all", action="store_true", help="Run all gates in order")
    parser.add_argument("--list", action="store_true", help="List available gates")
    args = parser.parse_args()

    if args.list:
        print("\n\033[1mAvailable Gates:\033[0m")
        for name in _GATE_REGISTRY:
            print(f"  --phase {name:<12} Gate {name}")
        return

    if args.all:
        all_passed = True
        for name in _GATE_REGISTRY:
            if not run_gate(name):
                all_passed = False
                print(f"\033[91mGate {name} failed. Stopping.\033[0m")
                sys.exit(1)
        if all_passed:
            print("\033[92m\033[1mAll gates PASSED.\033[0m")
        sys.exit(0 if all_passed else 1)

    if args.phase:
        success = run_gate(args.phase)
        sys.exit(0 if success else 1)

    parser.print_help()


if __name__ == "__main__":
    main()
