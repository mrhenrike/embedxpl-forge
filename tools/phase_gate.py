def _check_references_have_urls() -> CheckResult:
    """Verify every new module has at least one URL in references."""
    bad = []
    known_url_domains = ("nvd.nist.gov", "exploit-db.com", "github.com",
                         "packetstormsecurity.com", "cve.mitre.org",
                         "rapid7.com", "sploitus.com", "attackerkb.com")
    for path in _ALL_NEW_PATHS:
        if not path.exists():
            continue
        src = _read_file(path)
        ref_m = re.search(r"['\"]references['\"][^(]*\((.*?)\)", src, re.S)
        if ref_m:
            ref_block = ref_m.group(1)
            has_url = any(d in ref_block for d in known_url_domains)
            if not has_url:
                bad.append(path.name)
        else:
            bad.append(path.name)
    if bad:
        return _fail("references_urls", f"Missing known-source URL in references: {bad[:5]}")
    return _ok("references_urls", f"All {len(_ALL_NEW_PATHS)} modules have source URLs")


