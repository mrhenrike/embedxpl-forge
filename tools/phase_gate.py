# ─── Result dataclass ─────────────────────────────────────────────────────────

@dataclass
class CheckResult:
    name: str
    passed: bool
    message: str = ""


def _ok(name: str, msg: str = "") -> CheckResult:
    return CheckResult(name=name, passed=True, message=msg)


def _fail(name: str, msg: str) -> CheckResult:
    return CheckResult(name=name, passed=False, message=msg)


# ─── Shared helpers ───────────────────────────────────────────────────────────

def _read_file(path: Path) -> str:
    try:
        return path.read_text(encoding="utf-8", errors="replace")
    except Exception:
        return ""


def _run_python(snippet: str, timeout: int = 10) -> Tuple[bool, str]:
    """Run a Python snippet via subprocess and return (success, output)."""
    try:
        proc = subprocess.run(
            [sys.executable, "-c", snippet],
            capture_output=True, text=True, timeout=timeout,
            cwd=str(ROOT),
            env={**os.environ, "PYTHONPATH": str(ROOT), "PYTHONUTF8": "1"},
        )
        if proc.returncode == 0:
            return True, proc.stdout.strip()
        return False, (proc.stderr or proc.stdout).strip()
    except subprocess.TimeoutExpired:
        return False, "timeout"
    except Exception as e:
        return False, str(e)


def _run_python_file(script: str, timeout: int = 15) -> Tuple[bool, str]:
    """Write script to a temp file and run it."""
    tmp_dir = ROOT / ".tmp"
    tmp_dir.mkdir(exist_ok=True)
    tmp_file = tmp_dir / f"gate_check_{os.getpid()}_{int(time.time()*1000)}.py"
    header = f"import sys; sys.path.insert(0, {str(ROOT)!r})\n"
    tmp_file.write_text(header + script, encoding="utf-8")
    try:
        proc = subprocess.run(
            [sys.executable, str(tmp_file)],
            capture_output=True, text=True, timeout=timeout,
            cwd=str(ROOT),
            env={**os.environ, "PYTHONPATH": str(ROOT), "PYTHONUTF8": "1"},
        )
        ok = proc.returncode == 0
        out = proc.stdout.strip() if ok else (proc.stderr or proc.stdout).strip()
        return ok, out
    except subprocess.TimeoutExpired:
        return False, "timeout"
    except Exception as e:
        return False, str(e)
    finally:
        try:
            tmp_file.unlink(missing_ok=True)
        except Exception:
            pass


# ─── Common module checks (applied to every new module) ──────────────────────

def _common_module_checks(paths: List[Path]) -> List[CheckResult]:
    """Apply standard checks to a list of module paths."""
    results: List[CheckResult] = []

    for path in paths:
        name = path.stem

        # 1. File exists
        if not path.exists():
            results.append(_fail(f"exists:{name}", f"File not found: {path}"))
            continue

        src = _read_file(path)

        # 2. Has class Exploit
        if "class Exploit" not in src:
            results.append(_fail(f"class_exploit:{name}", f"{path.name}: no class Exploit"))
            continue

        # 3. __info__ with required keys
        for key in ("name", "description", "authors", "references", "devices"):
            if f'"{key}"' not in src and f"'{key}'" not in src:
                results.append(_fail(f"info_{key}:{name}", f"{path.name}: __info__ missing '{key}'"))

        # 4. references has at least one https:// URL
        ref_match = re.search(r"['\"]references['\"].*?(\(.*?\)|\[.*?\])", src, re.S)
        if ref_match:
            ref_block = ref_match.group(0)
            if "https://" not in ref_block and "http://" not in ref_block:
                results.append(_fail(f"references_url:{name}", f"{path.name}: __info__['references'] has no URL"))
        else:
            results.append(_fail(f"references_url:{name}", f"{path.name}: could not parse references field"))

        # 5. check() and run() exist with a body
        for method in ("check", "run"):
            pattern = rf"def {method}\s*\("
            if not re.search(pattern, src):
                results.append(_fail(f"{method}_exists:{name}", f"{path.name}: missing def {method}()"))
            else:
                # Ensure body is not just 'pass' or 'return'
                m = re.search(rf"def {method}\s*\([^)]*\)[^:]*:(.*?)(?=\ndef |\Z)", src, re.S)
                if m:
                    body = m.group(1).strip()
                    stripped = re.sub(r"#.*$", "", body, flags=re.M).strip()
                    if stripped in ("", "pass", "..."):
                        results.append(_fail(f"{method}_body:{name}", f"{path.name}: {method}() is a stub (empty body)"))

        # 6. No prohibited strings
        for prohibited in _PROHIBITED_STRINGS:
            if prohibited in src:
                results.append(_fail(f"prohibited:{name}", f"{path.name}: contains prohibited string '{prohibited[:30]}'"))

        # 7. No hardcoded lab IPs (except in comments or __info__)
        src_no_comments = re.sub(r"#.*$", "", src, flags=re.M)
        src_no_info = re.sub(r"__info__.*?^\s*\}", "", src_no_comments, flags=re.M | re.S)
        lab_ips = _LAB_IP_RE.findall(src_no_info)
        # Allow 192.168.x.x only in OptIP defaults and examples
        real_lab_ips = [ip for ip in lab_ips if ip not in ("192.168.1.1", "192.168.0.1", "192.168.1.100", "10.0.0.1")]
        if real_lab_ips:
            results.append(_fail(f"lab_ip:{name}", f"{path.name}: hardcoded lab IPs: {real_lab_ips[:3]}"))

        # 8. No subprocess for external tools (only socket/requests/http_client allowed)
        if re.search(r"\bsubprocess\b|\bos\.system\b|\bshlex\b", src):
            # Allow if used in bridge modules only
            if "bridge" not in name and "_bridge" not in str(path):
                results.append(_fail(f"no_subprocess:{name}", f"{path.name}: uses subprocess (not allowed in non-bridge modules)"))

    return results


def _check_importable(paths: List[Path]) -> List[CheckResult]:
    """Verify each module can be imported without error."""
    results: List[CheckResult] = []
    for path in paths:
        if not path.exists():
            continue
        dotted = (
            "embedxpl.modules."
            + str(path.relative_to(ROOT / "embedxpl" / "modules"))
            .replace(os.sep, ".")
            .removesuffix(".py")
        )
        script = (
            f"import warnings; warnings.filterwarnings('ignore')\n"
            f"import importlib\n"
            f"m = importlib.import_module({dotted!r})\n"
            f"assert hasattr(m, 'Exploit'), 'No Exploit class'\n"
            f"print('OK')\n"
        )
        ok, out = _run_python_file(script, timeout=15)
        if not ok:
            results.append(_fail(f"import:{path.stem}", f"{path.name}: import failed: {out[:200]}"))
        else:
            results.append(_ok(f"import:{path.stem}"))
    return results


def _check_anti_false_positive(paths: List[Path]) -> List[CheckResult]:
    """Verify check() returns False/None on closed port 63994 (never True)."""
    results: List[CheckResult] = []
    for path in paths:
        if not path.exists():
            continue
        dotted = (
            "embedxpl.modules."
            + str(path.relative_to(ROOT / "embedxpl" / "modules"))
            .replace(os.sep, ".")
            .removesuffix(".py")
        )
        script = (
            f"import warnings, threading\n"
            f"warnings.filterwarnings('ignore')\n"
            f"import importlib\n"
            f"m = importlib.import_module({dotted!r})\n"
            f"cls = m.Exploit\n"
            f"e = cls.__new__(cls)\n"
            f"e.target = '127.0.0.1'\n"
            f"e.port = 63994\n"
            f"try:\n"
            f"    init = getattr(type(e), '__init__', None)\n"
            f"    if init and init is not object.__init__: init(e)\n"
            f"except Exception: pass\n"
            f"result_box = [None]\n"
            f"def _run():\n"
            f"    try: result_box[0] = e.check()\n"
            f"    except Exception: result_box[0] = False\n"
            f"t = threading.Thread(target=_run, daemon=True)\n"
            f"t.start(); t.join(timeout=8)\n"
            f"if t.is_alive():\n"
            f"    print('TIMEOUT'); exit(0)\n"
            f"result = result_box[0]\n"
            f"assert not result, f'check() returned {{result!r}} on closed port'\n"
            f"print('OK')\n"
        )
        ok, out = _run_python_file(script, timeout=15)
        if not ok and "TIMEOUT" not in out:
            results.append(_fail(f"anti_fp:{path.stem}", f"{path.name}: {out[:200]}"))
        else:
            results.append(_ok(f"anti_fp:{path.stem}"))
    return results


def _check_indexed(stems: List[str]) -> CheckResult:
    """Verify all module stems appear in index_modules() output."""
    script = (
        "from embedxpl.core.exploit.utils import index_modules\n"
        "mods = index_modules()\n"
        "print('\\n'.join(mods))\n"
    )
    ok, out = _run_python_file(script, timeout=20)
    if not ok:
        return _fail("indexed", f"index_modules() failed: {out[:200]}")
    found = out.splitlines()
    missing = [s for s in stems if not any(s in m for m in found)]
    if missing:
        return _fail("indexed", f"Not indexed: {missing[:5]}")
    return _ok("indexed", f"All {len(stems)} modules indexed")


def _check_flake8(paths: List[Path]) -> CheckResult:
    """Run flake8 on all paths and fail on critical errors (E1/E2/E7/W6)."""
    py_paths = [str(p) for p in paths if p.exists()]
    if not py_paths:
        return _ok("flake8", "no files to check")
    try:
        proc = subprocess.run(
            [sys.executable, "-m", "flake8",
             "--select=E1,E2,E7,W6",
             "--extend-ignore=E501",
             "--per-file-ignores=embedxpl/modules/*:F401,F403,F405,E221",
             ] + py_paths,
            capture_output=True, text=True, timeout=60, cwd=str(ROOT),
        )
        if proc.returncode != 0 and proc.stdout.strip():
            lines = proc.stdout.strip().splitlines()
            return _fail("flake8", f"{len(lines)} issues: {lines[0]}")
        return _ok("flake8")
    except FileNotFoundError:
        return _ok("flake8", "flake8 not installed, skipping")
    except Exception as e:
        return _fail("flake8", str(e))


def _check_total_module_count(expected_min: int) -> CheckResult:
    script = (
        "from embedxpl.core.exploit.utils import index_modules\n"
        "mods = index_modules()\n"
        f"assert len(mods) >= {expected_min}, f'Only {{len(mods)}} modules, expected >={expected_min}'\n"
        "print(len(mods))\n"
    )
    ok, out = _run_python_file(script, timeout=20)
    if not ok:
        return _fail("total_module_count", out[:200])
    return _ok("total_module_count", f"{out} modules indexed (min {expected_min})")


